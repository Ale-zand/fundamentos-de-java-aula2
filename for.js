const numeros = [100, 200, 300, 400, 500, 600];

// Primeira expressão do for é executada apenas uma única vez
// Segunda expressão é uma condição de execução do for
// Terceira expressão é executada após o final de todo o bloco (após o fim de todo o for, incluindo o código interno)

for (let indice = 0; indice < numeros.length; indice++) {

    console.log(numeros[indice]);

}
